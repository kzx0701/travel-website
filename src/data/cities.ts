/**
 * 生成城市拼音（无音调全拼小写）
 * 对于多音字/特殊地名，pinyin-pro 已能正确处理绝大多数情况
 */
function toPinyin(name: string): string {
  return pinyin(name, { toneType: 'none', type: 'array', nonZh: 'removed' }).join('')
}

